print(9//4)  #9整除4=2
print(-9//-4)  # 整除等于2


print(9//-4)  # -3，原本是等于-2.25，但是规定向下取整，往小了取，所以是-3，不要问为什么，这就是规定
print(-9//4)    # -3，原本是等于-2.25，但是规定向下取整，往小了取，所以是-3，不要问为什么，这就是规定

#但是取余就比较麻烦了，得利用公式

print(9%-4)  #-3， 公式，  余数=被除数-除数*商      9-(-4)*(-3) =  -3, 这里的商是上面的结果
print(-9%4)  #3,  -9-4*(-3) = 3，这里的商是上面的结果